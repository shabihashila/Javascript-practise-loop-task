/***

Subtask-1:

Find all the odd numbers from 61 to 100.

 */
// use for loop
for (let i = 61; i >= 61 && i <= 100; i++) {
  if (i % 2 !== 0) {
    console.log("odd num:", i);
  }
}

/***

Subtask-2:

Find all the even numbers from 78 to 98.

 */
for (num = 78; num >= 78 && num <= 98; num++) {
  if (num % 2 === 0) {
    console.log("even num: ", num);
  }
}
