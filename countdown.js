/***

Implement a countdown timer that counts down from 81 to 65.

 */
for (let i = 81; i >= 65; i--) {
  console.log(i);
}
console.log("using for loop");

/***

Implement a countdown timer that counts down from 21 to 15.

 */
let n = 21;
while (n >= 15) {
  console.log(n);
  n--;
}
console.log("using while loop");
